/**
 * project-scaffold.js V2
 * Scaffolding complet d'un projet avec outils Windsurf premium V2
 * Includes staleness metadata and cross-platform awareness
 */

const fs = require('fs');
const path = require('path');

function scaffoldProject(projectName, config) {
  const basePath = path.resolve(projectName);
  const today = new Date().toISOString().split('T')[0];
  const interval = getReviewInterval(config.maturity || 'MVP');

  console.log(`Scaffolding project: ${projectName}`);
  console.log(`Config:`, JSON.stringify(config, null, 2));

  // Base directories
  const dirs = [
    '.devin/rules',
    '.devin/skills',
    '.devin/workflows',
    '.devin/rag',
    '.devin/integrations/mcp',
    '.devin/references',
    'docs',
    'src'
  ];

  dirs.forEach(dir => {
    const fullPath = path.join(basePath, dir);
    fs.mkdirSync(fullPath, { recursive: true });
    console.log(`  📁 ${dir}`);
  });

  // Base files
  const files = {
    'AGENTS.md': generateAgentsMd(config, today, interval),
    '.devin/rag/config.yaml': generateRagConfig(config),
    '.devin/integrations/mcp/servers.json': generateMcpConfig(config),
    'README.md': generateReadme(config)
  };

  Object.entries(files).forEach(([filePath, content]) => {
    fs.writeFileSync(path.join(basePath, filePath), content);
    console.log(`  📄 ${filePath}`);
  });

  console.log(`\n✅ Project scaffolded at: ${basePath}`);
  console.log(`   Staleness interval: ${interval} days (${config.maturity || 'MVP'})`);
  return basePath;
}

function getReviewInterval(maturity) {
  const intervals = { 'POC': 30, 'MVP': 60, 'production': 90, 'legacy-migration': 45 };
  return intervals[maturity] || 90;
}

function generateAgentsMd(config, date, interval) {
  return `# ${config.projectName} - AGENTS.md

## Rôle & Mission

Agent principal du projet **${config.projectName}**.
Stack: ${config.techStack} | Domaine: ${config.businessDomain}

## Règles associées

- \`coding-standards.md\` → Glob
- \`domain-knowledge.md\` → Model decision
- \`security-guardrails.md\` → Always on

## Skills disponibles

- \`@specification-brainstorm\`
- \`@tools-documentation\`

## Conventions

- Documentation: Français
- Code/Comments: Anglais
- Nommage: kebab-case

---
**Metadata staleness** : created=${date}, interval=${interval}d, generator=ppg-v2
`;
}

function generateRagConfig(config) {
  return `rag_version: "1.0"
project: "${config.projectName}"

sources:
  local:
    - path: "./src"
      boost: 1.5
      include_patterns: ["**/*.md", "**/*.txt"]
    - path: "./docs"
      boost: 2.0
  remote:
    - url: "https://docs.windsurf.com"
      boost: 1.0

queries:
  project-setup:
    description: "Informations de configuration du projet"
    keywords: ["setup", "configuration", "${config.techStack}"]
    top_k: 5
    filters:
      type: ["guidelines", "config"]
`;
}

function generateMcpConfig(config) {
  const servers = ["filesystem"];
  if (config.businessDomain === 'devops') servers.push("github");

  return JSON.stringify({
    mcpServers: servers.map(name => ({
      name,
      enabled: true,
      config: {}
    }))
  }, null, 2);
}

function generateReadme(config) {
  return `# ${config.projectName}

Generated by Premium Project Generator V2
Stack: ${config.techStack}
Domain: ${config.businessDomain}

## Windsurf Tools

See \`.devin/\` directory for Rules, Skills, Workflows, RAG and MCP configurations.

## Quick Start

1. Review \`AGENTS.md\`
2. Check \`.devin/rules/\`
3. Try \`/init-project\` workflow

## V2 Scripts

\`\`\`bash
# Validate the kit
python3 .devin/skills/premium-project-generator-v2/tools/scripts/validate-kit.py .

# Security scan
python3 .devin/skills/premium-project-generator-v2/tools/scripts/security-scan.py .

# Staleness check
python3 .devin/skills/premium-project-generator-v2/tools/scripts/staleness-check.py .
\`\`\`
`;
}

// CLI
const projectName = process.argv[2];
const configJson = process.argv[3];

if (!projectName) {
  console.error('Usage: node project-scaffold.js <project-name> \'{"techStack":"...",...}\'');
  process.exit(1);
}

const config = configJson ? JSON.parse(configJson) : {
  projectName,
  techStack: 'unknown',
  businessDomain: 'unknown',
  maturity: 'MVP'
};

scaffoldProject(projectName, config);
